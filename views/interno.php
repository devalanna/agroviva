      <h3>PECUÁRIA</h3>  
      <p>Pecuária é a atividade produtiva de criação de animais que tem como propósito a 
      produção de alimentos como carnes, ovos e leites, ou de matérias-primas, a exemplo de peles, 
      couros e lãs destinadas à confecção de outros artigos. A pecuária pode ser de corte, leiteira ou 
      de lã, e praticada nas modalidades extensiva ou intensiva. Não obstante ela possua grande importância
      para a economia e para a subsistência, pode causar alguns problemas ambientais, como o desmatamento, a
      compactação dos solos e a emissão de gases poluentes na atmosfera.
    </div>

